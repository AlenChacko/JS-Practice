let y = 5+10 // 5 and 10 are operands ,,,,, + is the operator

// Unary operator
// These operators operate on a single operand.
let x = "5";
console.log(+x); // 5



// Binary operator
// These operators work on two values.
console.log(10 + 5); // 15
console.log(10 % 3); // 1 (Remainder)
console.log(2 ** 3); // 8 (Exponentiation)



// Ternary operator
// The ternary operator (condition ? expr1 : expr2) is the only operator in JavaScript that takes three operands.
let age = 18;
let status = (age >= 18) ? "Adult" : "Minor";
console.log(status); // "Adult"

