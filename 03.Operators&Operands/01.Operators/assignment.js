// Compound Assignment Operators

let number = 100;
console.log((number += 5)); //number = number + 5
console.log((number -= 5)); //number = number - 5
console.log((number *= 5)); //number = number * 5
console.log((number /= 5)); //number = number / 5
console.log((number %= 5)); //number = number / 5
console.log((number **= 5)); //number = number ** 5


let name = "Code";
name += "Master";
console.log(name); // "CodeMaster"


