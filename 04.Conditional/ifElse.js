// 1. if Statement
let age = 18;

if (age >= 18) {
  console.log("You're an adult.");
}


// 2. if...else Statement
let isRaining = true;

if (isRaining) {
  console.log("Take an umbrella.");
} else {
  console.log("No umbrella needed.");
}


// 3. if...else if...else
let score = 75;

if (score >= 90) {
  console.log("Grade: A");
} else if (score >= 80) {
  console.log("Grade: B");
} else if (score >= 70) {
  console.log("Grade: C");
} else {
  console.log("Grade: F");
}

