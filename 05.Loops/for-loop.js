// Basic Syntax:
for (initialization; condition; increment/decrement) {
    // code to be executed
  }


//   Example: Simple for Loop
  for (let i = 0; i < 5; i++) {
    console.log(i);
  }


//   Example with Arrays
let fruits = ["apple", "banana", "cherry"];

for (let i = 0; i < fruits.length; i++) {
  console.log(fruits[i]);
}
